"""
DHT handler implementations for the DID:IIoT Verifiable Data Registry.

Two concrete handler classes are provided:

DHTHandler
    Pure-Python implementation backed by the AuthKademlia submodule and
    the dilithium_py library.  Suitable for environments without a Rust
    toolchain.

RustDHTHandler
    Rust-backed implementation using the authkademlia_py PyO3 extension
    (built from auth-kademlia-rs/).  Higher throughput and native Dilithium
    performance.

IMPORTANT — key format incompatibility:
    dilithium_py (Python) and pqcrypto_dilithium (Rust) use different internal
    byte layouts for secret keys.  Keys generated by one handler MUST NOT be
    passed to the other.

Both handlers share the same public interface:
    generate_did_iiot(), get_did_iiot(), get_did_iiot_suffix(),
    get_did_document(), insert_did_document_in_the_DHT(), key_rotation(),
    get_record_from_DHT(), revoke_did_iiot(), start_dht_service(),
    get_vc_from_authoritative_node()
"""

from AuthKademlia.modules import DilithiumKeyManager, KyberKeyManager, Server, DIDSignatureVerifierHandler, SIGNATURE_ALG_LENGTHS
from did_iiot.modules import DIDIndustrialIoT, Service, VerificationMethod, DIDDocument
import utils
