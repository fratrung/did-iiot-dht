    async def insert_did_document_in_the_DHT(self):
        """Sign the DID Document with the Rust key manager and publish it to the DHT."""
        if self.did_handler is None:
            return None
        dilith_sk = self._ensure_bytes(self.dilith_key_manager.get_private_key("k0"))
        value = self._build_signed_record(self.did_handler.did_document, dilith_sk)
